    private void jAtualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jAtualizarMouseClicked
        if (jComboContas.getSelectedIndex() >= 0){
            Contas contas = (Contas) jComboContas.getSelectedItem();
            contas.setSaldo(Float.parseFloat(jSaldo.getText()));
            contas.setTipoContas(jComboTipoContas.getSelectedItem().toString());
            contas.setInstituicaoFinanceira(jInstituicaoFinanceira.getText());
           

           try {
                servicocontas.update(contas);
                JOptionPane.showMessageDialog(rootPane, "Conta atualizada com sucesso!!");
                this.limparTela();
           } catch (SQLException ex) {
               Logger.getLogger(FEditDespesas.class.getName()).log(Level.SEVERE, null, ex);
           }
     
        }
        
    }//GEN-LAST:event_jAtualizarMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
       
        try {
            this.atualizarListaContas();
            jComboContas.setSelectedIndex(-1);
        } catch (SQLException ex) {
            Logger.getLogger(FEditContas.class.getName()).log(Level.SEVERE, null, ex);
        }

      
    }//GEN-LAST:event_formWindowActivated

    private void jComboContasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboContasItemStateChanged
       if (jComboContas.getItemCount() <= 0){
            return;
        }
        
        if (jComboContas.getSelectedIndex() < 0){
            return;
        } 
        
        Contas contas = (Contas) jComboContas.getSelectedItem();
        jSaldo.setText(String.valueOf(contas.getSaldo()));
        jComboTipoContas.setSelectedItem(contas.getTipoContas());
        jInstituicaoFinanceira.setText(contas.getInstituicaoFinanceira());   
    }//GEN-LAST:event_jComboContasItemStateChanged

