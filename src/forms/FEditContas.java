    private void atualizarListaContas() throws SQLException {
         if (jComboContas.getItemCount() > 0){
            jComboContas.removeAllItems();
        }

        ArrayList<Contas> lista = servicocontas.getListaContas();   
        for (Contas u:lista){
               jComboContas.addItem(u);
        }      
        this.limparTela();
    }
    
    public void limparTela(){
        jSaldo.setText("");
        jComboTipoContas.setSelectedIndex(-1);
        jInstituicaoFinanceira.setText("");     
    }
    
