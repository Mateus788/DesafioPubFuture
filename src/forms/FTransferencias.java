    private void jTransferirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTransferirMouseClicked
        if (jComboOrigem.getSelectedIndex() < 0){
            JOptionPane.showMessageDialog(rootPane, "Voce precisa selecionar uma conta de origem");
        }else{
            if(jComboDestino.getSelectedIndex() < 0){
                JOptionPane.showMessageDialog(rootPane, "Voce precisa selecionar uma conta de destino");
            }
        }
        
        Contas contadestino = (Contas) jComboDestino.getSelectedItem();  
        Contas contaorigem = (Contas) jComboOrigem.getSelectedItem(); 

        try {
            servicocontas.transferirOrigem(contaorigem, Float.parseFloat(jValor.getText()));
            servicocontas.transferirDestino(contadestino, Float.parseFloat(jValor.getText()));
        } catch (SQLException ex) {
            Logger.getLogger(FTransferencias.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.limparTela();
        jValor.setText("");
        
    }//GEN-LAST:event_jTransferirMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        try {
            this.atualizarListaContas();
            jComboOrigem.setSelectedIndex(-1);
        } catch (SQLException ex) {
            Logger.getLogger(FTransferencias.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_formWindowActivated

    private void jComboOrigemItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboOrigemItemStateChanged

        try {
            this.atualizarListaContasDestino();
            jComboDestino.removeItem(jComboOrigem.getSelectedItem());
        } catch (SQLException ex) {
            Logger.getLogger(FTransferencias.class.getName()).log(Level.SEVERE, null, ex);
        }       
       
    }//GEN-LAST:event_jComboOrigemItemStateChanged

