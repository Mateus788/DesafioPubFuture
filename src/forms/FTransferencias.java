    private void atualizarListaContas() throws SQLException {
        if (jComboOrigem.getItemCount() > 0){
            jComboOrigem.removeAllItems();
        }

        ArrayList<Contas> lista = servicocontas.getListaContas();   
        for (Contas u:lista){
               jComboOrigem.addItem(u);
        }      
        this.limparTela();
    }
  
    private void atualizarListaContasDestino() throws SQLException {
        if (jComboDestino.getItemCount() > 0){
            jComboDestino.removeAllItems();
        }

        ArrayList<Contas> lista = servicocontas.getListaContas();   
        for (Contas u:lista){
               jComboDestino.addItem(u);
        }      

        
        
        jComboDestino.setSelectedIndex(-1);
        
    }
    
    public void limparTela(){
        jComboDestino.setSelectedIndex(-1);
        jComboOrigem.setSelectedIndex(-1);
    }

